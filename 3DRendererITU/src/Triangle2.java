
public class Triangle2 {

	Point2 a;
	Point2 b;
	Point2 c;
	
	public Triangle2(Point2 a, Point2 b, Point2 c) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	public Point2 getA() {
		return a;
	}
	
	public void setA(Point2 a) {
		this.a = a;
	}
	
	public Point2 getB() {
		return b;
	}
	
	public void setB(Point2 b) {
		this.b = b;
	}
	
	public Point2 getC() {
		return c;
	}
	
	public void setC(Point2 c) {
		this.c = c;
	}	
	
}
